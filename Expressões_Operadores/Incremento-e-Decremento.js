// Incremento
let numero = 10
console.log("Numero antes de incrementar ", numero)
numero++
console.log("incremento de mais 1 igual a = ",numero)
++numero
console.log("Numero com o sinal de mais antes", ++numero)
console.log()

// Decremento
let numero2 = 10
numero2--
console.log("Decremento de menos 1" ,numero2)
